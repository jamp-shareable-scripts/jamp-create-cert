function getOpensslError() {
	$errorString = '';
	$temp = openssl_error_string();
	while ($temp) {
		$errorString .= $temp . PHP_EOL;
		$temp = openssl_error_string();
	}
	return $errorString;
}

function cdir($path) {
	return str_replace('\\', '\\\\', $path);
