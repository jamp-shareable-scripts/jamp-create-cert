$subject = ['commonName' => 'test.localhost'];
$privateKey = openssl_pkey_new([
	'private_key_type' => OPENSSL_KEYTYPE_EC,
	'curve_name' => 'prime256v1',
	'config' => $opensslCnfFilename
]);
if (!$privateKey) {
	throw new Error(getOpensslError());
}

$csr = openssl_csr_new($subject, $privateKey, [
	'digest_alg' => 'sha384',
	'config' => $caCnfFilename
]);
if (!$csr) {
	throw new Error(getOpensslError());
}

$x509 = openssl_csr_sign($csr, null, $privateKey, 365, [
	'digest_alg' => 'sha384',
	'config' => $caCnfFilename
]);
if (!$x509) {
	throw new Error(getOpensslError());
}

if (!openssl_x509_export_to_file($x509, 'ecc-cert.pem')) {
	throw new Error(getOpensslError());
};
if (!openssl_pkey_export_to_file($privateKey, 'ecc-private.key', '', [
	'config' => $opensslCnfFilename
])) {
	throw new Error(getOpensslError());
}

unlink($caCnfFilename);
unlink($opensslCnfFilename);

